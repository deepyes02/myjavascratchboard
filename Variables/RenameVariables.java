package Variables;

// In java, variables can be renamed. unless, they are marked as "final"; 
public class RenameVariables {
	public static void main(String[] args)
	{
		String name = "Amrit";
		System.out.println("Hi there, my name is "+name+"."); //hi there, my name is Amrit
		
		name = "Deepesh";
		System.out.println("Hi there, my name is "+name+"."); // hi there, my name is Deepesh
		
	}
}
