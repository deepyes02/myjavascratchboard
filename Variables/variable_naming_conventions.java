package Variables;

public class variable_naming_conventions {
	public static void main(String[] args){
		System.out.println("Names can contain letters, digits, underscore, and dollar signs");
		System.out.println("Names must begin with a letter");
		System.out.println("Name should start with a lowercase letter and it cannot contain whitespace");
		System.out.println("Names can also begin with $ and _");
		System.out.println("Names are case sensitive, 'myVar' and 'myvar' are different");
		System.out.println("Reserved words can not be used as variable names, eg, 'int', 'boolean' , etc");
	}
}
